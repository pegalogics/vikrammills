     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
            $clientlogo = Clientlogo::updateOrCreate(['id' => 1], $request->all());
            $file = $request->file('clientlogo');
            $filename = 'clientlogo.jpg';
            $file->move('uploads/clientlogo', $filename);
            $clientlogo->clientlogo = $filename;
            $clientlogo->save();
            return redirect()->route('clientlogo.index');
    }

